/* FACULTY TABS */
function facultyTabs() {

  var tabContainers2 = jQuery(".tab-container-faculty");
  tabContainers2.hide().filter(":first").show();
  
  jQuery("ul#tab-nav-faculty a").click(function () {
    tabContainers2.hide();
    tabContainers2.filter(this.hash).show();
    jQuery("ul#tab-nav-faculty a").removeClass("selected");
    jQuery(this).addClass("selected");
    return false;
  }).filter(":first").click();
  
  /* hide bio */
  jQuery(".bio").hide();

  /* show bio on click */
    jQuery(".tab-container-faculty ul li a").click(function() {
      jQuery(this).parent().find("div.bio").toggle();
      jQuery(this).parent().toggleClass("selected");
    });    

  /* remove background image if there's no bio */
    jQuery(".tab-container-faculty ul li:not(:has(div))").css("background","none");

}









